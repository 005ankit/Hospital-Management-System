    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        try{
            int id=Integer.parseInt(txtid.getText());
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","Admin");
            String q="delete from doctor where id=?";
            
            PreparedStatement ps=con.prepareStatement(q);
            ps.setInt(1,id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Data Deleted Successfully!!!");
            new viewdoctor().setVisible(true);
            dispose();
        }
        catch(Exception e){
            System.out.println("Error: "+e.getMessage());
            JOptionPane.showMessageDialog(this,"Please Enter Doctor Id");
        }
    }//GEN-LAST:event_btndeleteActionPerformed

